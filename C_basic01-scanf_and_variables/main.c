// this program can read 2 integers from keyboard and print the sum of them

#include <stdio.h>

int main(int argc, const char * argv[]) {
    
    int A;
    int B;
    int sumResult;
    
    // scanf 1st argument --> input from keyboard
    // scanf 2nd argument --> memory space for the variable which is getting the keyboard input
    
    scanf("%d", &A);
    scanf("%d", &B);
    printf("The sum result is %d", sumResult);
}
